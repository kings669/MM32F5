#include "board_it.h"
#include "board_init.h"
static uint64_t SysRunTimeMs = 0;
static uint8_t fac_us = 0;
static uint32_t fac_ms = 0;

__attribute__((section(".RAM_D1"))) uint8_t D1_array[8] = {0,}; 

void SysTick_Init(void)
{
		uint32_t reload = 0;

		SysTick->CTRL &= (uint32_t)0xFFFFFFFB;
    fac_us = CLOCK_SYS_FREQ / 8000000U;
    fac_ms = CLOCK_SYS_FREQ / 8000U;

    reload = CLOCK_SYSTICK_FREQ/1000;
    reload--;

    SysTick->CTRL |= SysTick_CTRL_TICKINT_Msk;
    SysTick->LOAD = reload;
    SysTick->CTRL |= SysTick_CTRL_ENABLE_Msk;
}

/**
  * @brief  This function handles SysTick Handler.
  * @param  None
  * @retval None
  */
void SysTick_Handler(void)
{
  SysRunTimeMs++;
}

uint32_t GetSysRunTimeMs(void)
{
	return SysRunTimeMs;
}

void delay_us(uint16_t nus)
{
    uint32_t ticks = 0;
    uint32_t told = 0;
    uint32_t tnow = 0;
    uint32_t tcnt = 0;
    uint32_t reload = 0;
    reload = SysTick->LOAD;
    ticks = nus * fac_us;
    told = SysTick->VAL;
    while (1)
    {
        tnow = SysTick->VAL;
        if (tnow != told)
        {
            if (tnow < told)
            {
                tcnt += told - tnow;
            }
            else
            {
                tcnt += reload - tnow + told;
            }
            told = tnow;
            if (tcnt >= ticks)
            {
                break;
            }
        }
    }
}

